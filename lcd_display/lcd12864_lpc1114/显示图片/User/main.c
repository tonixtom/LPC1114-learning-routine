/*******************************************************************************
* 函 数 名         : main
* 函数功能		   : 主函数
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/
int main(void)
{
	uint8_t i,j,n;
	LPC_GPIO2->DIR = 0xFFF;   		//设置端口2为输出方向
	LPC_GPIO2->DATA = 0xFFF;  	//端口输出高电平
	LPC_GPIO3->DIR = 0xFFF;   		//设置端口2为输出方向
	LPC_GPIO3->DATA = 0xFFF;  	//端口输出高电平
	LPC_SYSCON->SYSAHBCLKCTRL |= (1<<7);	//使能CT16B0时钟

	

	UART_init(9600);
	Lcd12864_Init();
	Lcd12864_ClearScreen();
	
	UART_send("begin   ",8);
	UART_send_byte('\n');
		

	for (i=0;i<8;i++)
	{  
	    //--设置初始显示开始位置--//
		LcdSt7565_WriteCmd(0x40);

		//--设置Y坐标--//
		LcdSt7565_WriteCmd(0xB0+i);
		
		//--设置X坐标--// 
		LcdSt7565_WriteCmd(0x10); 
		LcdSt7565_WriteCmd(0x04); 
		for(j=0; j<128; j++)
		{
			LcdSt7565_WriteData(~pic[n]);
			n=n+1;
		}
	}
	while (1)
	{
	}

}


